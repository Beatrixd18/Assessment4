package za.ac.cput.chapter4.ISP;


public class Calculator {
    public int add(int c, int d)
    {
        return c + d;
    }

    public int subtract(int c, int d)
    {
        return c - d;
    }

    public int multiple(int c, int d)
    {
        return c * d;
    }

    public int devide(int c, int d)
    {
        return c / d;
    }
}
